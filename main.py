def avg(a: int, b: int, c: int) -> float:
    """
    Compute the average of 3 numbers.

    Parameters
    ----------
    a, b, c : float
        The three numbers to average.

    Returns
    -------
    average : float
        The average of a, b, and c.
    """
    return (a + b + c) / 3


